// Función composable que aplica el tema visual a toda la app
@Composable
fun UnscrambleTheme(
    darkTheme: Boolean = isSystemInDarkTheme(), // Usa el tema oscuro si el sistema está en modo oscuro
    dynamicColor: Boolean = false, // Desactiva el uso de colores dinámicos (Android 12+)
    content: @Composable () -> Unit // Contenido de la UI al que se aplicará el tema
) {
    // Selección del esquema de colores dependiendo del modo oscuro y si se permiten colores dinámicos
    val colorScheme = when {
        // Si se permiten colores dinámicos y el dispositivo soporta Android 12 (S) o superior
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        // Si está activado el modo oscuro, usar paleta oscura
        darkTheme -> DarkColors
        // En caso contrario, usar paleta clara
        else -> LightColors
    }

    // Aplicación del tema global de Material 3 con colores, tipografía y formas personalizadas
    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content,
        shapes = Shapes
    )
}

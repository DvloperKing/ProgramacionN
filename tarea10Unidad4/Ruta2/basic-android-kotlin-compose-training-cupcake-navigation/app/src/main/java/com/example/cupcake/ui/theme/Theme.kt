/**
 * Función composable que aplica el tema Cupcake a toda la interfaz de usuario.
 *
 * @param darkTheme Indica si se debe usar el tema oscuro (por defecto detecta el modo del sistema)
 * @param dynamicColor Si es verdadero, usa colores dinámicos en Android 12+ (desactivado por defecto para entrenamiento)
 * @param content Bloque composable que recibe el tema aplicado
 */
@Composable
fun CupcakeTheme(
    darkTheme: Boolean = isSystemInDarkTheme(), // Detecta si el sistema está en modo oscuro
    dynamicColor: Boolean = false, // Colores dinámicos deshabilitados por defecto
    content: @Composable () -> Unit // Contenido composable que usará el tema
) {
    // Determina el esquema de color a utilizar basado en las condiciones dadas
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColors
        else -> LightColors
    }

    // Accede a la vista actual para configurar la barra de estado
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            // Establece el color de la barra de estado al color primario del esquema seleccionado
            window.statusBarColor = colorScheme.primary.toArgb()
            // Configura si los íconos de la barra de estado deben ser claros u oscuros según el tema
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = darkTheme
        }
    }

    // Aplica el tema a toda la jerarquía composable
    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography, // Tipografía definida en otro archivo
        content = content
    )
}

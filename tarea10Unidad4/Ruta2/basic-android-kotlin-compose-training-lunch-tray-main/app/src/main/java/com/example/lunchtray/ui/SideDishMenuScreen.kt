/**
 * Pantalla de selección de platillos secundarios. Reutiliza la estructura base del menú general.
 *
 * @param options Lista de acompañamientos disponibles.
 * @param onCancelButtonClicked Acción al presionar "Cancelar".
 * @param onNextButtonClicked Acción al presionar "Siguiente".
 * @param onSelectionChanged Acción al seleccionar un platillo secundario.
 * @param modifier Modificador opcional para estilo.
 */
@Composable
fun SideDishMenuScreen(
    options: List<SideDishItem>,
    onCancelButtonClicked: () -> Unit,
    onNextButtonClicked: () -> Unit,
    onSelectionChanged: (SideDishItem) -> Unit,
    modifier: Modifier = Modifier
) {
    BaseMenuScreen(
        options = options,
        onCancelButtonClicked = onCancelButtonClicked,
        onNextButtonClicked = onNextButtonClicked,
        onSelectionChanged = onSelectionChanged as (MenuItem) -> Unit,
        modifier = modifier
    )
}

/**
 * Vista previa en tiempo de diseño para desarrolladores.
 */
@Preview
